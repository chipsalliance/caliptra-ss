// ## LCC Transitions Smoke Test
// 
// This test checks, whether we can reach the next LC state using an unlock token.
// By using the gen_fuse_ctrl_vmem.py script, an arbitrary LC start state isprogrammed
// into the OTP memory.
// Then test reads the current state and tries to do a state + 1.
// 
// On each test execution, a random LC start state as well as LC counter is generated by
// the `gen_fuse_ctrl_vmem.py` script and placed into the `otp-img.2048.vmem` file.
#include <string.h>
#include <stdint.h>
#include <time.h>
#include <stdlib.h>

#include "soc_address_map.h"
#include "printf.h"
#include "riscv_hw_if.h"
#include "soc_ifc.h"
#include "fuse_ctrl_address_map.h"
